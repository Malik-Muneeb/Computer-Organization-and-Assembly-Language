.MODEL SMALL
.STACK 100H
.DATA
	INPUTMSG DB 10,13,"Enter a string: $"
	OUTPUTMSG DB 10,13,"Length: $" 
	NEWLINE DB 10,13,"$"
	STR1 DB 10 DUP('$')
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	MOV ES,AX
	
	;INPUT MESSEGE
	LEA DX,INPUTMSG
	MOV AH,09H
	INT 21H
	
	;INPUT A STRING
	LEA DI,STR1
	MOV CX,99

INPUT_STRING:	
	MOV AH,01H
	INT 21H
	CMP AL,0DH
	JE TERMINATE_INPUT
    STOSB		;AL TO DI
	LOOP INPUT_STRING

	
TERMINATE_INPUT:
	LEA DI,STR1
	MOV AL,'$'
	MOV CX,10
	REPNE SCASB
	
	MOV BX,9D
	SUB BX,CX
	ADD BX,30H
	
	;OUTPUT MESSEGE
	LEA DX,OUTPUTMSG
	MOV AH,09H
	INT 21H
	
	MOV AH,02H
	MOV DL,BL
	INT 21H
	
	MOV AH,4CH
	INT 21H
	
MAIN ENDP
END MAIN