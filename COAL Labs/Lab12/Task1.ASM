.MODEL SMALL
.STACK 100H
.DATA
	INPUTMSG DB 10,13,"Enter a string: $"
	OUTPUTMSG DB 10,13,"REVERSE IS: $" 
	NEWLINE DB 10,13,"$"
	STR1 DB 100 DUP('$')
	STR2 DB 100 DUP('$')
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	MOV ES,AX
	
	;INPUT MESSEGE
	LEA DX,INPUTMSG
	MOV AH,09H
	INT 21H
	
	;INPUT A STRING
	LEA DI,STR1
	MOV CX,99

INPUT_STRING:	
	MOV AH,01H
	INT 21H
	CMP AL,0DH
	JE TERMINATE_INPUT
    STOSB		;AL TO DI
	LOOP INPUT_STRING

	
TERMINATE_INPUT:
	MOV BX,99
	SUB BX,CX
	MOV CX,BX
	
	MOV SI, DI
	DEC SI
	LEA DI,STR2
	
MOVE_TO_STORE_STR2:	
	MOVSB		;SI TO DI
	DEC SI
	DEC SI
	LOOP MOVE_TO_STORE_STR2
	
	;OUTPUT MESSEGE
	LEA DX,OUTPUTMSG
	MOV AH,09H
	INT 21H
	
	LEA DX,STR2
	MOV AH,09H
	INT 21H
	
	MOV AH,4CH
	INT 21H
	
MAIN ENDP
END MAIN