.MODEL SMALL
.STACK 100H
.DATA
	PROMPT1 DB 10, 13, "Enter first character: $"
	PROMPT2 DB 10, 13, "Enter second character: $"
	OUTPUT1 DB 10, 13, "First character = $"
	OUTPUT2 DB 10, 13, "Second character = $"
	CH1 DB ?
	CH2 DB ?
.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX

	MOV DX, OFFSET PROMPT1
	MOV AH, 09
	INT 21H

	MOV AH,01
	INT 21H

	MOV CH1,AL

	MOV DX, OFFSET PROMPT2
	MOV AH, 09
	INT 21H

	MOV AH,01
	INT 21H

	MOV CH2,AL

	MOV AL,CH1
	XCHG CH2,AL
	MOV CH1,AL

	MOV DX, OFFSET OUTPUT1
	MOV AH, 09
	INT 21H

	MOV AH,02
	MOV AL,CH1
	INT 21H

	MOV DX, OFFSET OUTPUT2
	MOV AH, 09
	INT 21H

	MOV AH,02
	MOV AL,CH2
	INT 21H

	MOV AH, 4CH
	INT 21H
MAIN ENDP
END MAIN
