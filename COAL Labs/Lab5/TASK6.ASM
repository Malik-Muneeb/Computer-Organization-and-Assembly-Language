.MODEL SMALL
.STACK 100H
.DATA
	PROMPT1 DB 10,13, "Enter first word:  $"
	PROMPT2 DB 10,13, "Enter second word:  $"
	OUTPUT DB 10,13, "In Pig-Latin: $"
	ARR1 DB 6 DUP('$')
	ARR2 DB 6 DUP('$')
	STR1 DB "ay $"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	LEA DX,PROMPT1
	MOV AH,09
	INT 21H
	
	LEA SI,ARR1
	MOV CX,5
	MOV AH,01
INPUT1:
	INT 21H
	MOV [SI],AL
	INC SI
	LOOP INPUT1
	
	LEA DX,PROMPT2
	MOV AH,09
	INT 21H
	
	LEA SI,ARR2
	MOV CX,5
	MOV AH,01
INPUT2:
	INT 21H
	MOV [SI],AL
	INC SI
	LOOP INPUT2

	LEA SI,ARR1
	MOV DL,[SI]
	INC SI
	LEA DI,ARR1
	MOV CX,4
LATIN1:
	MOV AL,[SI]
	MOV [DI],AL
	INC SI
	INC DI
	LOOP LATIN1
	
	MOV [DI],DL
	
	LEA SI,ARR2
	MOV DL,[SI]
	INC SI
	LEA DI,ARR2
	MOV CX,4
LATIN2:
	MOV AL,[SI]
	MOV [DI],AL
	INC SI
	INC DI
	LOOP LATIN2
	
	MOV [DI],DL
	
	LEA DX,OUTPUT
	MOV AH,09
	INT 21H
	
	LEA DX,ARR1
	MOV AH,09
	INT 21H
	
	LEA DX,STR1
	MOV AH,09
	INT 21H
	
	LEA DX,ARR2
	MOV AH,09
	INT 21H
	
	LEA DX,STR1
	MOV AH,09
	INT 21H
	MOV AH,4CH
	INT 21H
MAIN ENDP
END MAIN
