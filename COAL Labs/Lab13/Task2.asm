.MODEL SMALL
.STACK 100H
.DATA
	PROMPT1 DB 10,13,"Enter first number: $"
	PROMPT2 DB 10,13,"Enter second number: $"
	PROMPT3 DB 10,13,"GCD is: $"
	NEWLINE DB 10,13,"$"
INCLUDE DECIMAL.ASM
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	LEA DX,PROMPT1
	MOV AH,09H
	INT 21H
	
	CALL DECINP
	PUSH BX		;FIRST NUMBER IN STACK

	LEA DX,PROMPT2
	MOV AH,09H
	INT 21H
	
	CALL DECINP
	
	LEA DX,PROMPT3
	MOV AH,09H
	INT 21H
	
	POP AX 		;FIRST NUMBER OUT OF STACK
	
CHECK:	
	CMP AX,0
	JL WHILE_END
	CMP BX,0
	JL WHILE_END
	
	CMP AX,BX
	JLE SECOND
	SUB AX,BX
	JMP CHECK
	
SECOND:
	CMP BX,AX
	JLE WHILE_END
	SUB BX,AX
	MOV BX,BX
	JMP CHECK

WHILE_END:

	PUSH AX
	CALL DECOUT
		
	MOV AH,4CH
	INT 21H
	

MAIN ENDP
END MAIN