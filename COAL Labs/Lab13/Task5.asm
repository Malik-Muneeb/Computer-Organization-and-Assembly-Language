.MODEL SMALL
.STACK 100H
.DATA
	PROMPT1 DB 10,13,"Enter number of minutes:  $"
	PROMPT2 DB 10,13,"These minutes is equivalent to $"
	NEWLINE DB 10,13,"$"
	DAYS DB " days, $"
	HOURS DB " hours, $"
	MINUTES DB " minutes.$"

INCLUDE DECIMAL.ASM
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	LEA DX,PROMPT1
	MOV AH,09H
	INT 21H
	
	CALL DECINP
	
	LEA DX,PROMPT2
	MOV AH,09H
	INT 21H
	
	
	MOV DX,0
	MOV AX,BX
	MOV BX,1440
	CMP AX,BX
	JL LESS_THAN_DAYS
	DIV BX
	JMP CALC_HOURS
LESS_THAN_DAYS:
	MOV CX,0
	PUSH CX
	JMP CALC_HOURS1
	
CALC_HOURS:		
	PUSH AX 		;DAYS 
	MOV AX,DX

CALC_HOURS1:	
	MOV DX,0
	MOV BX,60
	CMP AX,BX
	JL LESS_THAN_HOURS
	DIV BX
	JMP CALC_MINUTES

LESS_THAN_HOURS:
	MOV CX,0
	PUSH CX
	JMP CALC_MINUTES1
	
CALC_MINUTES:	
	PUSH AX			;HOURS
	PUSH DX			;MINUTES
	JMP REMAIN
	
CALC_MINUTES1:
	PUSH AX			;MINUTES
	
REMAIN:	
	POP CX
	POP BX
	POP AX
	
	PUSH AX
	CALL DECOUT
	
	LEA DX,DAYS
	MOV AH,09H
	INT 21H
	
	
	PUSH BX
	CALL DECOUT
	
	LEA DX,HOURS
	MOV AH,09H
	INT 21H
	
	PUSH CX
	CALL DECOUT
	
	LEA DX,MINUTES
	MOV AH,09H
	INT 21H
	
	MOV AH,4CH
	INT 21H
	
MAIN ENDP
END MAIN