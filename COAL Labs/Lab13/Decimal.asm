.CODE

;DECIMAL INPUT START
DECINP PROC
	PUSH AX
	PUSH DX
	MOV BX,0
	
DECINP_AGAIN:
	MOV AH,01
	INT 21H
	CMP AL,0DH
	JE DECINP_AGAIN_END
	SUB AL,30H
	MOV AH,0
	PUSH AX
	MOV AX,10D
	MOV DX,0
	MUL BX
	MOV BX,AX
	POP AX
	ADD BX,AX
	JMP DECINP_AGAIN
	
DECINP_AGAIN_END:

	POP DX
	POP AX
	RET

DECINP ENDP
;DECIMAL INPUT ENDP


;DECIMAL OUTPUT START
DECOUT PROC
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	PUSH BP
	MOV BP,SP
	MOV AX,[BP+12]
	CMP AX,0
	JNE DECOUT_COUNT
	PUSH AX
	MOV CX,1
	JMP DECOUT_AGAIN_END
	
DECOUT_COUNT:
	MOV CX,0
	
DECOUT_AGAIN:
	CMP AX,0
	JE DECOUT_AGAIN_END
	MOV BX,10
	MOV DX,0
	DIV BX
	PUSH DX
	INC CX
	JMP DECOUT_AGAIN
	
DECOUT_AGAIN_END:

	POP DX
	ADD DX,30H
	MOV AH,02
	INT 21H
	LOOP DECOUT_AGAIN_END
	
EXIT:
	POP BP
	POP DX
	POP CX
	POP BX
	POP AX
	RET 2

DECOUT ENDP
;DECIMAL OUTPUT END