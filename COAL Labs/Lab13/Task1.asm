.MODEL SMALL
.STACK 100H
.DATA
	PROMPT1 DB 10,13,"Enter a number: $"
	PROMPT2 DB 10,13,"Factorial of $"
	PROMPT3 DB " is: $"
	NEWLINE DB 10,13,"$"
INCLUDE DECIMAL.ASM
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	LEA DX,PROMPT1
	MOV AH,09H
	INT 21H
	
	CALL DECINP
	MOV CX,BX
	
	
	LEA DX,PROMPT2
	MOV AH,09H
	INT 21H
	
	ADD CX,30H
	MOV DX,CX
	MOV AH,02
	INT 21H
	
	LEA DX,PROMPT3
	MOV AH,09H
	INT 21H
	
	XOR AX,AX
	MOV AX,1D
	MOV DX,0
FACT:
	CMP BX,0
	JE TERMINATE_FACT
	MUL BX
	DEC BX
	JMP FACT

TERMINATE_FACT:

	PUSH AX
	CALL DECOUT
	
	
	MOV AH,4CH
	INT 21H
	
MAIN ENDP
END MAIN