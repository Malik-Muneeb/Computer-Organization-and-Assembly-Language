.MODEL SMALL
.STACK 100H
.DATA
	NEWLINE DB 10,13,"$"
	V DB ?
.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS,AX
	
	MOV CX,128
	MOV V,0H
	MOV DL,0h
	
REPEAT1:

	MOV BH, 10
	MOV DX, OFFSET NEWLINE
	MOV AH,09
	INT 21H
	MOV DL,V
REPEAT2:
	
	DEC BH
	JZ REPEAT1
	MOV AH,02
	INT 21H
	INC DL
	MOV V,DL
	DEC CX
	JNZ REPEAT2
	
	MOV AH,4CH
	INT 21H
MAIN ENDP
END MAIN