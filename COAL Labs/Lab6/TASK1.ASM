.MODEL SMALL
.STACK 100H
.DATA
	PROMPT1 DB 10,13,"Enter a hex digit (A-F): $"
	PROMPT2 DB 10,13,"In decimal , it is: 1$"
.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS,AX
	
	MOV DX, OFFSET PROMPT1
	MOV AH,09
	INT 21H
	
	MOV AH,01
	INT 21H
	
	SUB AL, 17
	
	MOV DX, OFFSET PROMPT2
	MOV AH,09
	INT 21H
	
	MOV AH,02
	MOV DL, AL
	INT 21H
	
	MOV AH,4CH
	INT 21H
MAIN ENDP
END MAIN