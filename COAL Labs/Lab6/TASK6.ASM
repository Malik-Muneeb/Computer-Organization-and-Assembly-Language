.MODEL SMALL
.STACK 100H
.DATA
	PROMPT1 DB 10,13,"Enter a string of size 12: $"
	PROMPT2 DB 10,13,"Uppercase equivalent: $"
	NEWLINE DB 10,13,"$"
	STR1 DB 12 DUP(?)
.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS,AX
	
	MOV DX, OFFSET PROMPT1
	MOV AH,09
	INT 21H
	
	MOV CH,12
	LEA SI, STR1
REPEAT1:	
	MOV AH,01
	INT 21H
	MOV [SI],AL
	INC SI
	DEC CH
	JNZ REPEAT1
	
	MOV DX, OFFSET PROMPT2
	MOV AH,09
	INT 21H
	
	MOV CH,12
	LEA SI, STR1
	
REPEAT2:	
	MOV AH,02
	MOV DL,[SI]
	SUB DL,32
	INT 21H
	INC SI
	DEC CH
	JNZ REPEAT2
	
	MOV AH,4CH
	INT 21H
MAIN ENDP
END MAIN