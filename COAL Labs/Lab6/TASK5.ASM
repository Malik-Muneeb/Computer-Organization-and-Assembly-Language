.MODEL SMALL
.STACK 100H
.DATA
	PROMPT1 DB 10,13,"Enter Size: $"
	NEWLINE DB 10,13,"$"
	VAR1 DB ?
	VAR2 DB ?
.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS,AX
	
	MOV DX, OFFSET PROMPT1
	MOV AH,09
	INT 21H
	
	MOV AH,01
	INT 21H
	
	MOV DX, OFFSET NEWLINE
	MOV AH,09
	INT 21H
	
	SUB AL,48
	MOV CH,AL
	MOV VAR1,0
	MOV VAR2,AL
MAINAGAIN:	
	
	INC VAR1
	MOV CL,VAR1
	DEC VAR2
	JZ AGAIN
	MOV BL, VAR2
SPACE:
	MOV DL,' '
	MOV AH,02
	INT 21H
	DEC BL
	JNZ SPACE
AGAIN:
	MOV DL,'*'
	MOV AH,02
	INT 21H
	DEC CL
	JNZ AGAIN
	
	MOV DX, OFFSET NEWLINE
	MOV AH,09
	INT 21H
	
	DEC CH
	JNZ MAINAGAIN
	MOV AH,4CH
	INT 21H
MAIN ENDP
END MAIN