.MODEL SMALL
.STACK 100H
.DATA
	PROMPT1 DB 10,13,"Enter Size: $"
	NEWLINE DB 10,13,"$"
	VAR DB ?
	VAR1 DB ?
.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS,AX
	
	MOV DX, OFFSET PROMPT1
	MOV AH,09
	INT 21H
	
	MOV AH,01
	INT 21H
	
	MOV DX, OFFSET NEWLINE
	MOV AH,09
	INT 21H
	
	SUB AL,48
	MOV CH,AL
	MOV VAR,AL
	SUB VAR,2
	MOV CL,AL
	MOV VAR1, AL
FIRST:

	MOV DL,'*'
	MOV AH,02
	INT 21H
	DEC CL
	JNZ FIRST
	SUB CH,2
	
	MOV DX, OFFSET NEWLINE
	MOV AH,09
	INT 21H
	
MAINAGAIN:	
	
	MOV CL,VAR
	MOV DL,'*'
	MOV AH,02
	INT 21H
AGAIN:
	
	MOV DL,' '
	MOV AH,02
	INT 21H
	DEC CL
	JNZ AGAIN
	
	MOV DL,'*'
	MOV AH,02
	INT 21H
	
	MOV DX, OFFSET NEWLINE
	MOV AH,09
	INT 21H
	
	DEC CH
	JNZ MAINAGAIN
	
	MOV CL,VAR1
LAST:
	MOV DL,'*'
	MOV AH,02
	INT 21H
	DEC CL
	JNZ LAST
	
	MOV DX, OFFSET NEWLINE
	MOV AH,09
	INT 21H	
	
	MOV AH,4CH
	INT 21H
MAIN ENDP
END MAIN