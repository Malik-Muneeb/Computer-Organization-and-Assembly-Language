.MODEL SMALL
.STACK 100H
.DATA
		PROMPT1 DB 10,13,"Enter a String: $"
		PROMPT2 DB 10,13,"Reverse is $"
		STR1 DB 100 DUP(?)
.CODE
MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		MOV DX,OFFSET PROMPT1
		MOV AH,09
		INT 21H
		
		MOV CX,100
		MOV BL,1
		LEA SI,STR1
		
AGAIN:
		MOV AH,01
		INT 21H
		
		CMP AL,13
		JE REMAIN
		MOV [SI],AL
		INC SI
		INC BL
		LOOP AGAIN
		
REMAIN:		
		MOV DX,OFFSET PROMPT2
		MOV AH,09
		INT 21H
		
AGAINOUTPUT:

		MOV DL,[SI]
		MOV AH,02
		INT 21H
		DEC SI
		DEC BL
		JNZ AGAINOUTPUT
				
		MOV AH,4CH
		INT 21H
MAIN ENDP
END MAIN