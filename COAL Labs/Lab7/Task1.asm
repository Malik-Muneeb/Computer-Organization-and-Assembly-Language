.MODEL SMALL
.STACK 100H
.DATA
		PROMPT1 DB 10,13,"ENTER A CHARACTER: $"
		NEWLINE DB 10,13,"$"
		PROMPT2 DB " BELONGS TO CATEGORY: $"
		LOWER DB "LOWERCASE$"
		UPPER DB "UPPERCASE$"
		DIGIT DB "DIGIT$"
		OPERATOR DB "MATHEMATICAL OPERATOR$"
		OTHER DB "OTHERS$"
.CODE
MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		MOV DX,OFFSET PROMPT1
		MOV AH,09
		INT 21H
		
		MOV AH,01
		INT 21H
		
		MOV DX,OFFSET NEWLINE
		MOV AH,09
		INT 21H
		
		CMP AL,'a'
		JL REMAIN
		CMP AL,'z'
		JG REMAIN
		MOV AH,02
		MOV DL,AL
		INT 21H
		
		MOV DX,OFFSET PROMPT2
		MOV AH,09
		INT 21H
		
		MOV DX,OFFSET LOWER
		MOV AH,09
		INT 21H
		JMP END1
		
REMAIN:
		CMP AL,'A'
		JB REMAIN1
		CMP AL,'Z'
		JA REMAIN1
		MOV AH,02
		MOV DL,AL
		INT 21H
		
		MOV DX,OFFSET PROMPT2
		MOV AH,09
		INT 21H
		
		MOV DX,OFFSET UPPER
		MOV AH,09
		INT 21H
		JMP END1

REMAIN1:
		CMP AL,'0'
		JB REMAIN2
		CMP AL,'9'
		JA REMAIN2
		MOV AH,02
		MOV DL,AL
		INT 21H
		
		MOV DX,OFFSET PROMPT2
		MOV AH,09
		INT 21H
		
		MOV DX,OFFSET DIGIT
		MOV AH,09
		INT 21H
		JMP END1
		
REMAIN2:
		CMP AL,'+'
		JE DISPLAY
		CMP AL,'-'
		JE DISPLAY
		CMP AL,'*'
		JE DISPLAY
		CMP AL,'/'
		JE DISPLAY
		CMP AL,'%'
		JE DISPLAY
		JMP REMAIN3

DISPLAY:	
		MOV AH,02
		MOV DL,AL
		INT 21H
	
		MOV DX,OFFSET PROMPT2
		MOV AH,09
		INT 21H
		
		MOV DX,OFFSET OPERATOR
		MOV AH,09
		INT 21H
		JMP END1

REMAIN3:		
		MOV AH,02
		MOV DL,AL
		INT 21H
	
		MOV DX,OFFSET PROMPT2
		MOV AH,09
		INT 21H
		
		MOV DX,OFFSET OTHER
		MOV AH,09
		INT 21H

END1:
		MOV AH,4CH
		INT 21H
MAIN ENDP
END MAIN