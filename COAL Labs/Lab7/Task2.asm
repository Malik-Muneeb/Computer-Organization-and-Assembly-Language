.MODEL SMALL
.STACK 100H
.DATA
		PROMPT1 DB 10,13,"ENTER STRING: $"
		PROMPT2 DB 10,13,"LENGTH: $"
		STR1 DB 100 DUP(?)
.CODE
MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		MOV DX,OFFSET PROMPT1
		MOV AH,09
		INT 21H
		
		MOV CX,100
		LEA SI,STR1
		MOV BL,0
AGAIN:
		MOV AH,01
		INT 21H
		
		CMP AL,13
		JE REMAIN
		MOV [SI],AL
		INC SI
		INC BL
		DEC CX
		LOOP AGAIN

REMAIN:
		
		MOV DX,OFFSET PROMPT2
		MOV AH,09
		INT 21H
		
		MOV DL,BL
		ADD DL,30H
		MOV AH,02
		INT 21H
		
		MOV AH,4CH
		INT 21H
MAIN ENDP
END MAIN