.MODEL SMALL
.STACK 1000H
.DATA
		PROMPT1 DB 10,13,"Enter a String: $"
		PROMPT2 DB 10,13,"It is palindrome. $"
		PROMPT3 DB 10,13,"It is not palindrome. $"
		STR1 DB 100 DUP(?)
.CODE
MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		MOV DX,OFFSET PROMPT1
		MOV AH,09
		INT 21H
		
		LEA SI,STR1
		MOV CX,100
		MOV BX,0
		
AGAININPUT:
		MOV AH,01
		INT 21H
		
		CMP AL,13
		JE REMAIN
		MOV [SI],AL
		INC SI
		INC BX
		DEC CX
		JNZ AGAININPUT

REMAIN:		
		DEC SI
		LEA DI,STR1
		MOV CX,BX
COMPARE:
		MOV BL,[SI]
		MOV BH,[DI]
		CMP BL,BH
		JNE NOTPALINDROME
		DEC SI
		INC DI
		LOOP COMPARE
		
		MOV DX,OFFSET PROMPT2
		MOV AH,09
		INT 21H
		JMP TERMINATE

NOTPALINDROME:
		MOV DX,OFFSET PROMPT3
		MOV AH,09
		INT 21H
TERMINATE:
		MOV AH,4CH
		INT 21H
MAIN ENDP
END MAIN